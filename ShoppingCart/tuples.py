#Tuples

my_tuples = (1, 2, 3, 4, 5)

print(my_tuples)

#access individual elements in a tuples
my_tuples = (1, 2, 3, 4, 5)

print(my_tuples)
print(my_tuples[0])
print(my_tuples[2])
print(my_tuples[-1]) # negative gives the last value

#Operators that can be used in tuples
tuple1 = (1, 2 , 3)
tuple2 = (4, 5, 6)

conc_tuple = tuple1 + tuple2   # using concatenation tuple
print(conc_tuple)

rep_tuple = tuple1 * 3   # using Representation tuple
print(rep_tuple)